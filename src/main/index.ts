import { app, BrowserWindow, ipcMain, globalShortcut } from "electron";
import { createOverlaysFolder, getOverlaysData } from "./utils/overlaysFolder";
import { createOverlayWindow } from "./utils/createOverlayWindow";
import path from "path";
import { IS_DEV, OVERLAYS_PATH } from "./main-constants";
import overlayServer from "./overlay-server";
import {
  ASSETS_SERVER_PORT,
  OVERLAY_SERVER_PORT,
} from "../shared/shared-constants";
import { createOverlaySettingsFile } from "./utils/overlaySettingsFile";
import AssetsServer from "./services/assetsServer/AssetsServer";
import { GameWebSocketServer } from "./services/gameWebSocketServer/GameWebSocketServer";
// This allows TypeScript to pick up the magic constants that's auto-generated by Forge's Webpack
// plugin that tells the Electron app where to look for the Webpack-bundled app code (depending on
// whether you're running in development or production).
declare const MAIN_WINDOW_WEBPACK_ENTRY: string;
declare const MAIN_WINDOW_PRELOAD_WEBPACK_ENTRY: string;

const windows: BrowserWindow[] = [];

// Handle creating/removing shortcuts on Windows when installing/uninstalling.
if (require("electron-squirrel-startup")) {
  app.quit();
}

const gameWsServer = new GameWebSocketServer();
gameWsServer.start();
const assetsServer = new AssetsServer(ASSETS_SERVER_PORT);
assetsServer.start();

createOverlaysFolder();
createOverlaySettingsFile();
overlayServer.listen(OVERLAY_SERVER_PORT, "localhost", () => {
  console.log("Overlay server is listening at port: " + OVERLAY_SERVER_PORT);
});

const createWindow = (): BrowserWindow => {
  // Create the browser window.
  const mainWindow = new BrowserWindow({
    height: 800,
    width: 1200,
    minWidth: 640,
    minHeight: 420,
    webPreferences: {
      preload: MAIN_WINDOW_PRELOAD_WEBPACK_ENTRY,
      nodeIntegration: true,
    },
    frame: false,
    titleBarStyle: "hidden",
  });

  mainWindow.setMenuBarVisibility(false);
  mainWindow.webContents.setAudioMuted(true);

  if (IS_DEV) {
    // Open the DevTools.
    mainWindow.webContents.openDevTools();
  }

  // and load the index.html of the app.
  mainWindow.loadURL(MAIN_WINDOW_WEBPACK_ENTRY);

  mainWindow.on("close", () => {
    windows.forEach((win) => {
      win.removeAllListeners("close"); // Remove the close prevention listener
      win.close(); // Close the overlay window
    });

    app.quit();
  });

  ipcMain.on("main-message", (_, data) => {
    if (data === "get-mod-names") {
      getOverlaysData();
    }
  });

  ipcMain.on("title-bar-message", (_, data) => {
    if (data === "close") {
      mainWindow.close();
    } else if (data === "minimize") {
      mainWindow.minimize();
    } else if (data === "restore") {
      if (mainWindow.isMaximized()) {
        mainWindow.restore();
      } else {
        mainWindow.maximize();
      }
    }
  });

  return mainWindow;
};

// This method will be called when Electron has finished
// initialization and is ready to create browser windows.
// Some APIs can only be used after this event occurs.
app.on("ready", () => {
  windows.push(createWindow());

  const modFolders = getOverlaysData();

  for (let i = 0; i < modFolders.length; i++) {
    windows.push(
      createOverlayWindow(
        path.join(OVERLAYS_PATH, modFolders[i].folderName, "index.html"),
        {
          width: 500,
          height: 200,
        },
      ),
    );

    /* Getting an overlay from dev server */

    // windows.push(
    //   createOverlayWindow("http://localhost:5173", {
    //     width: 500,
    //     height: 200,
    //   })
    // );
  }
});

// Quit when all windows are closed, except on macOS. There, it's common
// for applications and their menu bar to stay active until the user quits
// explicitly with Cmd + Q.
app.on("window-all-closed", () => {
  if (process.platform !== "darwin") {
    app.quit();
  }
});

app.on("activate", () => {
  // On OS X it's common to re-create a window in the app when the
  // dock icon is clicked and there are no other windows open.
  if (BrowserWindow.getAllWindows().length === 0) {
    createWindow();
  }
});

// In this file you can include the rest of your app's specific main process
// code. You can also put them in separate files and import them here.

let windowsHidden = false;
let windowsClickThrough = false;

app.whenReady().then(() => {
  // Register a 'CommandOrControl+X' shortcut listener.
  let ret = globalShortcut.register("CommandOrControl+Alt+H", () => {
    for (let i = 1; i < windows.length; i++) {
      const win = windows[i];
      if (windowsHidden) {
        win.show();
      } else {
        win.hide();
      }
      windowsHidden = !windowsHidden;
    }
  });

  if (!ret) {
    console.log("registration failed");
  }

  ret = globalShortcut.register("CommandOrControl+Alt+I", () => {
    for (let i = 1; i < windows.length; i++) {
      const win = windows[i];
      if (windowsClickThrough) {
        win.setIgnoreMouseEvents(false);
      } else {
        win.setIgnoreMouseEvents(true);
      }
      windowsClickThrough = !windowsClickThrough;
    }
  });
});

app.on("will-quit", () => {
  // Unregister all shortcuts.
  globalShortcut.unregisterAll();
});
